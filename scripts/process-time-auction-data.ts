// HTML解析辅助函数
function extractTextAfterLabel(content: string, label: string): string {
  const regex = new RegExp(`${label}[\\s\\S]*?\\n([\\s\\S]*?)(?=\\n#{1,6}|$)`, 'i');
  const match = content.match(regex);
  return match ? match[1].trim() : '';
}

function extractBulletPoints(content: string, section: string): string[] {
  const sectionContent = extractTextAfterLabel(content, section);
  if (!sectionContent) return [];
  
  const bullets = sectionContent.split('\n')
    .filter(line => line.trim().startsWith('*'))
    .map(line => line.replace(/^\*\s*/, '').trim())
    .filter(Boolean);
  
  return bullets;
}

function extractOrganizationInfo(content: string) {
  const nameMatch = content.match(/### ([^#\n]+)(?=\n\nhttps?:\/\/)/);
  const websiteMatch = content.match(/(https?:\/\/[^\s]+)/);
  
  return {
    name: nameMatch ? nameMatch[1].trim() : '',
    website: websiteMatch ? websiteMatch[1] : ''
  };
}

function extractServiceDate(content: string): string {
  const match = content.match(/服務日期\s*\n\n([^\n]+)/);
  return match ? match[1].trim() : '';
}

function extractLocation(content: string): string {
  const match = content.match(/服務地點\s*\n\n([^\n]+)/);
  return match ? match[1].trim() : '';
}

function extractTime(content: string): string {
  const match = content.match(/服務時數\s*\n\n([^\n]+)/);
  return match ? match[1].trim() : '';
}

function extractCauses(content: string): string[] {
  // 提取机构名称之后到"於"之前的标签
  const causePattern = /^([^#\n]+?)(?=\n\n於)/m;
  const match = content.match(causePattern);
  if (!match) return [];
  
  const lines = match[1].split('\n').filter(line => line.trim() && !line.includes('Foundation') && !line.includes('Association'));
  return lines.map(line => line.trim()).filter(Boolean);
}

function determineApplicationStatus(content: string): string {
  if (content.includes('截止報名') || content.includes('後截止')) {
    return 'Open for applications';
  }
  return 'Application closed';
}

function extractSpecialProgram(content: string) {
  if (content.includes('Swire Trust Go-Givers Program')) {
    return {
      name: 'Swire Trust Go-Givers Program',
      description: 'Swire Trust Go-Givers Program aims to encourage skilled volunteers to support Swire Trust NGO partners in education, marine conservation, and arts.'
    };
  }
  return undefined;
}

// 主要处理函数
function processProjectData(data: typeof scrapedData[0]): TimeAuctionProject {
  const content = data.content;
  
  // 提取项目标题和描述
  const titleMatch = content.match(/## ([^#\n]+)/);
  const descMatch = content.match(/## [^#\n]+\n\n([^\n]+)/);
  
  // 提取机构信息
  const orgInfo = extractOrganizationInfo(content);
  const causes = extractCauses(content);
  const orgDescription = extractTextAfterLabel(content, '我們的工作');
  
  // 提取项目详情
  const whatWeNeed = extractBulletPoints(content, '我們需要');
  const background = extractTextAfterLabel(content, '背景資料');
  const whatWeHave = extractTextAfterLabel(content, '我們可提供的資源');
  const whyImportant = extractTextAfterLabel(content, '對我們的意義');
  
  // 提取要求信息
  const skills = extractBulletPoints(content, '技能');
  const experienceLevel = extractBulletPoints(content, '經驗等級');
  const languages = extractBulletPoints(content, '語言');
  const ageRange = extractTextAfterLabel(content, '年齡範圍');
  
  return {
    project_id: data.id,
    project_url: data.url,
    project_title: titleMatch ? titleMatch[1].trim() : '',
    project_description: descMatch ? descMatch[1].trim() : '',
    organization: {
      name: orgInfo.name,
      website: orgInfo.website,
      causes: causes,
      description: orgDescription
    },
    project_details: {
      what_we_need: whatWeNeed,
      background: background,
      what_we_have: whatWeHave,
      why_important: whyImportant,
      project_period: extractServiceDate(content),
      location: extractLocation(content)
    },
    requirements: {
      time: extractTime(content),
      skills: skills,
      experience_level: experienceLevel,
      language: languages,
      age_range: ageRange || 'No age requirement'
    },
    special_program: extractSpecialProgram(content),
    posting_info: {
      posted: 'Recently posted',
      application_status: determineApplicationStatus(content)
    },
    similar_opportunities: [], // 暂时为空，可以后续添加
    scraped_at: new Date().toISOString().split('T')[0],
    scraped_content_type: 'complete_project_page'
  };
}

